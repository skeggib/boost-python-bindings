#include "calculator.hpp"

int Calculator::add(int lhs, int rhs) const
{
    return lhs + rhs;
}

int Calculator::substract(int lhs, int rhs) const
{
    return lhs - rhs;
}

int Calculator::multiply(int lhs, int rhs) const
{
    return lhs * rhs;
}

int Calculator::divide(int lhs, int rhs) const
{
    return lhs / rhs;
}
